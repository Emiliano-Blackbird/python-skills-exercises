# Reemplazo una palabraen una cadena de texto
texto = "Hola, mundo!"
texto_reemplazado = texto.replace("mundo", "Python")

print(f"La cadena original es: {texto}")
print(f"La cadena con la palabra reemplazada es: {texto_reemplazado}")

# Carácter reemplazado
texto = texto.replace("a", "o")
print(f"La cadena con el carácter 'a' reemplazado por 'o' es: {texto}")
